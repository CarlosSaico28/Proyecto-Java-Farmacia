    private void btnEnviarCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarCorreoActionPerformed
try {
    String correo = "carlos.saico.g28@gmail.com";
    String contra = "okclktkhhqwbfjex"; // Usa la contraseña de aplicación generada
    String correoDestino = lblCorreoFAC.getText();

    // Propiedades SMTP para usar SSL en lugar de STARTTLS
    Properties p = new Properties();
    p.put("mail.smtp.host", "smtp.gmail.com");
    p.put("mail.smtp.port", "465");  // Puerto para SSL
    p.put("mail.smtp.auth", "true");
    p.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory"); // Usar socket SSL
    p.put("mail.smtp.socketFactory.fallback", "false");
    p.put("mail.smtp.ssl.protocols", "TLSv1.2");  // Forzar TLS 1.2

    // Crear sesión
    Session s = Session.getInstance(p, new javax.mail.Authenticator() {
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(correo, contra);
        }
    });
    s.setDebug(true);

    // Crear mensaje
    Message mensaje = new MimeMessage(s);
    mensaje.setFrom(new InternetAddress(correo));
    mensaje.setRecipient(Message.RecipientType.TO, new InternetAddress(correoDestino));
    mensaje.setSubject("Envio de archivos adjuntos");

    // Cuerpo del mensaje
    MimeBodyPart texto = new MimeBodyPart();
    texto.setText("Mensaje enviado desde Java.");

    // Archivo adjunto
    MimeBodyPart adjunto = new MimeBodyPart();
    int numero = Integer.parseInt(txtCABID.getText());
    String rutaArchivo = "C:\\Users\\carlo\\OneDrive\\Escritorio\\Proyectos-U\\JAVA\\NuevaInterfaz\\src\\pdf\\venta" + numero + ".pdf";
    adjunto.setDataHandler(new DataHandler(new FileDataSource(rutaArchivo)));
    adjunto.setFileName("venta" + numero + ".pdf");

    // Ensamblar partes
    Multipart multipart = new MimeMultipart();
    multipart.addBodyPart(texto);
    multipart.addBodyPart(adjunto);
    mensaje.setContent(multipart);

    // Enviar mensaje
    Transport.send(mensaje);

    JOptionPane.showMessageDialog(this, "Mensaje enviado correctamente");

} catch (Exception ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error al enviar el correo: " + ex.getMessage());
}

    }//GEN-LAST:event_btnEnviarCorreoActionPerformed

